//! The Phase-4 player-pool lifecycle (`TRANSFER_MODEL.md` §8): the two ends
//! development alone leaves untouched. Left to itself the pool only drains
//! (aging, and once P4.5 can release players, unsigned attrition) or turns
//! geriatric — either way `TRANSFER_MODEL.md` §11's decade-long metrics would
//! measure a confounded pool, not market dynamics.
//!
//! **Youth intake** (§8.1): an annual cohort per club at the summer window,
//! reusing `worldgen::gen_player` with a tight 16-18 age band. Quality
//! anchors on the club's `reputation` scaled by its `coaching_milli`
//! multiplier — the second genuine consumer of the academy lever,
//! `worldgen::resolve_coaching` being the first.
//!
//! **Retirement** (§8.2): age ≥ `min_retirement_age` and either a best-role
//! CA below a league-relevance floor, or a full season spent unsigned.
//! Retired players stay in `World.players` (the log still references them in
//! historical `MatchPlayed` XIs) but leave every roster; `Player::retired` is
//! the marker every other consumer (`club_ai::observe`'s candidate filter,
//! `development::tick_changes`'s implicit exclusion via roster absence) can
//! check.
//!
//! Both fire at the same summer-window boundary `commands::transfer_window_events`
//! already fires the market on — one more emitter beside `dev_ticks_between`
//! and `market::resolve_window`, not a new tick mechanism.

use crate::development::DevKnobs;
use crate::event::Event;
use crate::rng::{Rng, derive_stream};
use crate::worldgen::gen_player;
use fforge_domain::{
    GameDate, Player, PlayerId, ROLE_WEIGHTS, Role, World, best_role, date::DAYS_PER_YEAR,
};
use std::collections::BTreeMap;

/// Tag namespace for the per-window youth-intake RNG stream
/// (`rng::derive_stream`), following the `TRANSFER_MODEL.md` §5 convention
/// (`"TRAN"`/`"FINA"`/`"YOUT"`). The window index is OR'd into the low bits,
/// so every summer window draws an independent stream.
pub const YOUTH_STREAM_NS: u64 = 0x594F_5554_0000_0000; // "YOUT"

/// The pool's own knob table (`TRANSFER_MODEL.md` §8): a plausibility-picked
/// starting point, sibling of `DevKnobs`/`ValueKnobs`/`UtilityKnobs`/
/// `MarketKnobs`/`FinanceKnobs` — a §11-harness re-fit target like the rest
/// of them (this task's scope fence is `DevKnobs` specifically, not this
/// brand-new table).
#[derive(Debug, Clone, Copy, PartialEq)]
pub struct PoolKnobs {
    /// Youths generated per club per summer window (§8.1).
    pub youth_cohort_size: usize,
    /// §8.2: a player younger than this never retires, regardless of CA or
    /// contract status.
    pub min_retirement_age: i32,
    /// §8.2's "league-relevance floor": a ≥-`min_retirement_age` player whose
    /// best-role CA has fallen below this retires outright.
    pub relevance_floor: u8,
}

impl Default for PoolKnobs {
    fn default() -> Self {
        PoolKnobs {
            youth_cohort_size: 2,
            min_retirement_age: 34,
            // Picked empirically against a real 15-season run, not a low
            // "obviously irrelevant" number: `DEVELOPMENT_MODEL.md`'s own
            // aging law lets mental/technical composites plateau rather than
            // crash (`career_arc`'s "physicals decline hard while mental
            // holds"), so a 34+ player's best-role CA routinely plateaus in
            // the high 50s-60s and never revisits a low floor — a `45` floor
            // left most veterans immortal, filling every squad and blocking
            // intake, and league mean age climbed past 37 inside 15 seasons.
            // `62` sits inside that plateau band, so a real fraction of every
            // aging cohort crosses below it each window — the release valve
            // `TRANSFER_MODEL.md` §8 needs opposite intake to actually close
            // the loop, not just a floor that is technically implemented.
            relevance_floor: 62,
        }
    }
}

/// One club's youth cohort (§8.1): up to `knobs.youth_cohort_size` fresh
/// 16-18s (fewer if `cohort_size` was already capped by the caller to the
/// club's roster headroom — see `summer_pool_events`), generated by reusing
/// `worldgen::gen_player`. `quality` anchors on the club's `reputation` —
/// the same scale `gen_player`'s `club_quality` argument already expects,
/// since `worldgen::resolve_reputation` derives reputation from that very
/// quality anchor — scaled by the club's coaching multiplier, so a
/// well-coached academy turns out a better cohort than an
/// identically-reputable club with a poor one. `next_id` is threaded through
/// and advanced so consecutive clubs in the same window never collide.
fn youth_cohort(
    rng: &mut Rng,
    reputation: u8,
    coaching_milli: u16,
    cohort_size: usize,
    next_id: &mut u32,
    today: GameDate,
    dev_knobs: &DevKnobs,
) -> Vec<Player> {
    let quality = reputation as f64 * (coaching_milli as f64 / 1000.0);
    (0..cohort_size)
        .map(|_| {
            let role = Role::ALL[rng.below(Role::ALL.len() as u32) as usize];
            let age = 16 + rng.below(3) as i32; // §8.1: a 16-18 age band
            let id = PlayerId(*next_id);
            *next_id += 1;
            gen_player(rng, id, role, quality, age, today, dev_knobs)
        })
        .collect()
}

/// Every player who retires this summer window (§8.2): age ≥
/// `min_retirement_age` and (best-role CA below `relevance_floor` or gone a
/// full season unsigned). `unsigned_since` is `GameState`'s bookkeeping of
/// the date each currently contract-less player last lost his contract (set
/// on `PlayerReleased`, cleared on re-signing) — "a full season" reads as one
/// sim-year elapsed since then. Already-retired players are excluded so a
/// re-run never emits a second `PlayerRetired` for the same player.
fn retirements(
    world: &World,
    today: GameDate,
    unsigned_since: &BTreeMap<PlayerId, GameDate>,
    knobs: &PoolKnobs,
) -> Vec<PlayerId> {
    world
        .players
        .values()
        .filter(|p| !p.retired && p.age(today) >= knobs.min_retirement_age)
        .filter(|p| {
            let (_, ca) = best_role(&p.attributes, &ROLE_WEIGHTS);
            let below_floor = ca < knobs.relevance_floor;
            let full_season_unsigned = p.contract.is_none()
                && unsigned_since
                    .get(&p.id)
                    .is_some_and(|since| today.days - since.days >= DAYS_PER_YEAR);
            below_floor || full_season_unsigned
        })
        .map(|p| p.id)
        .collect()
}

/// The summer window's pool events (`TRANSFER_MODEL.md` §8): one
/// `YouthIntake` per club with headroom (`World.clubs` order —
/// deterministic), then every qualifying `PlayerRetired` (`World.players`
/// order). Pure function of its inputs; its only randomness is
/// `derive_stream(seed, YOUTH_STREAM_NS | window_index)`, drawn in club-id
/// order, so replay reproduces an identical cohort set — a club's headroom
/// (and therefore how much of the stream it consumes) is itself fully
/// determined by prior, already-deterministic world state, so branching on
/// it introduces no nondeterminism. Retirement itself draws no RNG (a pure
/// filter), so it never perturbs the youth stream's position.
///
/// **`squad_max` caps intake to roster headroom.** §6's hard squad-bound
/// stabilizer (`club_ai::UtilityPolicy`'s `squad_max`, passed in rather than
/// re-encoded — the same "one source of truth" discipline `SQUAD_TEMPLATE`
/// gets) already stops the market from *buying* a club past it; without the
/// same cap here, intake alone can walk a club straight through it whenever
/// the market can't find buyers fast enough to absorb the cohort — a squad
/// only grows, never sheds, once nobody wants what's on offer. A club
/// already at or above `squad_max` gets no cohort at all this window.
#[allow(clippy::too_many_arguments)]
pub fn summer_pool_events(
    world: &World,
    today: GameDate,
    seed: u64,
    window_index: u64,
    unsigned_since: &BTreeMap<PlayerId, GameDate>,
    dev_knobs: &DevKnobs,
    knobs: &PoolKnobs,
    squad_max: usize,
) -> Vec<Event> {
    let mut rng = derive_stream(seed, YOUTH_STREAM_NS | window_index);
    let mut next_id = world.players.keys().next_back().map_or(0, |id| id.0 + 1);

    let mut events = Vec::with_capacity(world.clubs.len() + 4);
    for (&club_id, club) in &world.clubs {
        let room = squad_max.saturating_sub(club.players.len());
        let cohort_size = knobs.youth_cohort_size.min(room);
        let players = youth_cohort(
            &mut rng,
            club.reputation,
            club.coaching_milli,
            cohort_size,
            &mut next_id,
            today,
            dev_knobs,
        );
        if !players.is_empty() {
            events.push(Event::YouthIntake {
                date: today,
                club: club_id,
                players,
            });
        }
    }

    for player in retirements(world, today, unsigned_since, knobs) {
        events.push(Event::PlayerRetired {
            date: today,
            player,
        });
    }

    events
}

