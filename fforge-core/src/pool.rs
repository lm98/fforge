    /// Drive a real `Session` (worldgen → AI lineups → match engine →
    /// monthly development/finance/market/pool ticks) across `seasons` full
    /// seasons — the same real-pipeline discipline `career_arc::trace_seed`
    /// uses, since a synthetic cohort can't exercise the summer-window
    /// scheduling this task is actually about.
    fn run_seasons(seed: u64, seasons: usize) -> crate::Session {
        let log = crate::new_game(seed, &WorldGenConfig::default(), ClubId(0));
        let mut session = crate::Session::from_events(log, &mut []);
        for s in 0..seasons {
            while !session.state.season_over() {
                session
                    .execute(crate::Command::AdvanceMatchday, &mut [])
                    .expect("advance matchday");
            }
            if s + 1 < seasons {
                session
                    .execute(crate::Command::StartNextSeason, &mut [])
                    .expect("start next season");
            }
        }
        session
    }

    fn active_players(world: &World) -> Vec<&Player> {
        world.players.values().filter(|p| !p.retired).collect()
    }

    fn mean_age(world: &World, today: GameDate) -> f64 {
        let active = active_players(world);
        active.iter().map(|p| p.age(today) as f64).sum::<f64>() / active.len() as f64
    }

    /// `TRANSFER_MODEL.md` §11: "League mean age ... stable" — the whole
    /// reason this task exists (P4.7's decade-long metrics measure a
    /// draining or geriatric pool otherwise). Pooled over a couple of seeds
    /// (`career_arc`'s "treat multi-seed pooling as a project-wide harness
    /// invariant"), a deliberately wide sanity band per the same doc's §11
    /// "wide bands, gross-regression tripwires — not fit gates": it must
    /// neither drain toward a geriatric squad (youth intake too small/absent)
    /// nor crash toward academy age (intake wildly outpacing retirement).
    #[test]
    fn league_mean_age_is_stable_across_fifteen_seasons() {
        let seasons = 15;
        for seed in [1u64, 2u64] {
            let log = crate::new_game(seed, &WorldGenConfig::default(), ClubId(0));
            let mut session = crate::Session::from_events(log, &mut []);
            let start_mean = mean_age(&session.state.world, session.state.date);

            for s in 0..seasons {
                while !session.state.season_over() {
                    session
                        .execute(crate::Command::AdvanceMatchday, &mut [])
                        .expect("advance matchday");
                }
                if s + 1 < seasons {
                    session
                        .execute(crate::Command::StartNextSeason, &mut [])
                        .expect("start next season");
                }
            }

            let end_mean = mean_age(&session.state.world, session.state.date);
            assert!(
                (18.0..=32.0).contains(&end_mean),
                "seed {seed}: mean age {end_mean:.2} drifted outside a believable band after {seasons} seasons"
            );
            assert!(
                (end_mean - start_mean).abs() < 6.0,
                "seed {seed}: mean age drifted from {start_mean:.2} to {end_mean:.2} over {seasons} seasons"
            );
        }
    }

    /// `TRANSFER_MODEL.md` §11: "squad sizes ... all in [18,30]" plus the
    /// pool-as-a-whole not exploding or collapsing — the second half of
    /// closing the loop at both ends. Every club's roster is checked against
    /// the market's own hard stabilizer bounds (`UtilityKnobs::squad_min`/
    /// `squad_max`); the league-wide active (non-retired) population is
    /// checked against a wide multiple of its starting size, wide enough to
    /// tolerate the §12.3-flagged free-agent-pool drift without being a fit
    /// gate.
    #[test]
    fn pool_size_stays_bounded_across_fifteen_seasons() {
        let start_active = {
            let (world, _s, _d) = generate(3, &WorldGenConfig::default());
            active_players(&world).len()
        };

        let session = run_seasons(3, 15);
        let world = &session.state.world;

        let uk = crate::UtilityKnobs::default();
        for club in world.clubs.values() {
            assert!(
                (uk.squad_min..=uk.squad_max).contains(&club.players.len()),
                "{} ended with {} players, outside [{}, {}]",
                club.name,
                club.players.len(),
                uk.squad_min,
                uk.squad_max
            );
        }

        let end_active = active_players(world).len();
        assert!(
            end_active >= start_active / 2 && end_active <= start_active * 3,
            "active pool size drifted from {start_active} to {end_active} over 15 seasons — \
             outside a wide [0.5x, 3x] sanity band"
        );
    }

    /// `TRANSFER_MODEL.md` §8.2: a retired player "leaves every roster and
    /// [is] excluded from ... the market." Scans the whole log chronologically
    /// (append-only, so log order is calendar order): once a `PlayerRetired`
    /// is seen for a player, that id must never again appear in a
    /// `MatchPlayed` XI (a lineup) or as a `TransferCompleted` party (a bid
    /// that won).
    #[test]
    fn retired_players_never_appear_in_a_lineup_or_a_completed_transfer_again() {
        let session = run_seasons(5, 15);
        let mut retired: std::collections::BTreeSet<PlayerId> = std::collections::BTreeSet::new();
        let mut checked_any = false;

        for event in &session.log {
            match event {
                Event::PlayerRetired { player, .. } => {
                    retired.insert(*player);
                }
                Event::MatchPlayed {
                    home_xi, away_xi, ..
                } => {
                    for &pid in home_xi.iter().chain(away_xi) {
                        assert!(
                            !retired.contains(&pid),
                            "retired player {pid:?} appeared in a MatchPlayed XI"
                        );
                        checked_any = true;
                    }
                }
                Event::TransferCompleted { player, .. } => {
                    assert!(
                        !retired.contains(player),
                        "retired player {player:?} appeared in a completed transfer"
                    );
                    checked_any = true;
                }
                _ => {}
            }
        }

        assert!(
            !retired.is_empty(),
            "no PlayerRetired at all over 15 seasons — retirement never fired"
        );
        assert!(
            checked_any,
            "no MatchPlayed/TransferCompleted events to check at all"
        );
    }
