    #[test]
    fn youth_intake_adds_one_cohort_per_club_with_fresh_ids_in_the_16_to_18_band() {
        let cfg = WorldGenConfig {
            num_clubs: 4,
            ..Default::default()
        };
        let (world, _schedule, start) = generate(13, &cfg);
        let max_existing = *world.players.keys().next_back().unwrap();

        let events = summer_pool_events(
            &world,
            start,
            13,
            0,
            &BTreeMap::new(),
            &DevKnobs::default(),
            &PoolKnobs::default(),
            SQUAD_MAX,
        );

        let intakes: Vec<&Event> = events
            .iter()
            .filter(|e| matches!(e, Event::YouthIntake { .. }))
            .collect();
        assert_eq!(intakes.len(), world.clubs.len(), "one YouthIntake per club");

        for e in &intakes {
            let Event::YouthIntake { players, .. } = e else {
                unreachable!()
            };
            assert_eq!(players.len(), PoolKnobs::default().youth_cohort_size);
            for p in players {
                assert!(
                    p.id > max_existing,
                    "youth id must not collide with an existing player"
                );
                let age = p.age(start);
                assert!((16..=18).contains(&age), "youth age {age} outside 16-18");
                assert!(
                    p.contract.is_some(),
                    "an intake youth is signed to his club"
                );
            }
        }

        // No id collisions across clubs' cohorts either.
        let mut ids: Vec<PlayerId> = intakes
            .iter()
            .flat_map(|e| match e {
                Event::YouthIntake { players, .. } => {
                    players.iter().map(|p| p.id).collect::<Vec<_>>()
                }
                _ => unreachable!(),
            })
            .collect();
        let before = ids.len();
        ids.sort();
        ids.dedup();
        assert_eq!(ids.len(), before, "no duplicate ids across cohorts");
    }

    #[test]
    fn intake_is_capped_to_squad_max_headroom() {
        // §6's hard squad-bound stabilizer must also gate intake (this
        // task's own bug fix, caught by `pool_size_stays_bounded_across_fifteen_seasons`):
        // a club already at the cap gets no cohort at all; a club with only
        // one slot of room gets a one-player cohort, never the full
        // `youth_cohort_size`.
        let cfg = WorldGenConfig {
            num_clubs: 4,
            ..Default::default()
        };
        let (mut world, _schedule, start) = generate(31, &cfg);
        let ids: Vec<ClubId> = world.clubs.keys().copied().collect();
        let (full, one_room, plenty_room) = (ids[0], ids[1], ids[2]);
        let squad_max = 26usize;

        let template: Vec<PlayerId> = world.club(plenty_room).players.clone();
        // Pad `full` up to the cap exactly, and `one_room` to one below it,
        // by borrowing ids from a template roster (values don't matter here,
        // only roster length).
        let mut extra = 1000u32;
        while world.club(full).players.len() < squad_max {
            let pid = PlayerId(extra);
            extra += 1;
            let mut p = world.player(template[0]).clone();
            p.id = pid;
            world.players.insert(pid, p);
            world.clubs.get_mut(&full).unwrap().players.push(pid);
        }
        while world.club(one_room).players.len() < squad_max - 1 {
            let pid = PlayerId(extra);
            extra += 1;
            let mut p = world.player(template[0]).clone();
            p.id = pid;
            world.players.insert(pid, p);
            world.clubs.get_mut(&one_room).unwrap().players.push(pid);
        }

        let events = summer_pool_events(
            &world,
            start,
            31,
            0,
            &BTreeMap::new(),
            &DevKnobs::default(),
            &PoolKnobs::default(),
            squad_max,
        );

        let cohort_of = |club: ClubId| -> usize {
            events
                .iter()
                .filter_map(|e| match e {
                    Event::YouthIntake {
                        club: c, players, ..
                    } if *c == club => Some(players.len()),
                    _ => None,
                })
                .next()
                .unwrap_or(0)
        };

        assert_eq!(cohort_of(full), 0, "a club at squad_max must get no cohort");
        assert_eq!(
            cohort_of(one_room),
            1,
            "a club one slot under squad_max must get a one-player cohort"
        );
        assert_eq!(
            cohort_of(plenty_room),
            PoolKnobs::default().youth_cohort_size,
            "a club with plenty of room must get the full cohort"
        );
    }

    #[test]
    fn better_coached_reputation_matched_clubs_produce_better_cohorts_on_average() {
        // The academy lever (§8.1): at equal reputation, a higher
        // coaching_milli must skew the cohort's quality up, on average over
        // many windows (a single 2-player cohort is too small to test
        // per-window).
        let cfg = WorldGenConfig {
            num_clubs: 2,
            ..Default::default()
        };
        let (mut world, _schedule, start) = generate(4, &cfg);
        let ids: Vec<ClubId> = world.clubs.keys().copied().collect();
        let (lo, hi) = (ids[0], ids[1]);
        world.clubs.get_mut(&lo).unwrap().reputation = 60;
        world.clubs.get_mut(&hi).unwrap().reputation = 60;
        world.clubs.get_mut(&lo).unwrap().coaching_milli = 850;
        world.clubs.get_mut(&hi).unwrap().coaching_milli = 1150;

        let knobs = PoolKnobs {
            youth_cohort_size: 6,
            ..PoolKnobs::default()
        };
        let mut lo_sum = 0f64;
        let mut hi_sum = 0f64;
        let mut n = 0f64;
        for seed in 0..30u64 {
            let events = summer_pool_events(
                &world,
                start,
                seed,
                0,
                &BTreeMap::new(),
                &DevKnobs::default(),
                &knobs,
                SQUAD_MAX,
            );
            for e in &events {
                let Event::YouthIntake { club, players, .. } = e else {
                    continue;
                };
                let mean_ca: f64 = players
                    .iter()
                    .map(|p| best_role(&p.attributes, &ROLE_WEIGHTS).1 as f64)
                    .sum::<f64>()
                    / players.len() as f64;
                if *club == lo {
                    lo_sum += mean_ca;
                } else {
                    hi_sum += mean_ca;
                }
            }
            n += 1.0;
        }
        assert!(
            hi_sum / n > lo_sum / n,
            "better coaching at equal reputation should raise mean youth CA: lo {} vs hi {}",
            lo_sum / n,
            hi_sum / n
        );
    }

